package Pulpo::Exception::RequestTimeout;

use strict;
use warnings;

use base qw(Pulpo::Exception);

sub new {
   my $that = shift;
   my $proto = ref($that) || $that;
   my $self = { @_ };

   bless($self, $proto);

   return $self;
}

1;
